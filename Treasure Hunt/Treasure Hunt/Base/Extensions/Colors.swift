import UIKit

extension UIColor {
    
    class var greenButtonColor: UIColor {
        return .systemGreen.withAlphaComponent(0.4)
    }
    
    class var redButtonColor: UIColor {
        return .systemRed.withAlphaComponent(0.4)
    }

}
